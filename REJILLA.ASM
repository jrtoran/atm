; REJILLA VIDA UTIL
; RUTINA DIVISION POR 10 EN R24 VALOR A DIVIDIR
; RESULTADO EN R5 Y EN R24 RESTO

VIDA_UTIL:



	LDS				R24, RAM_PRO
	RCALL			DIV
	MOV				R24, R18
; MIDE LA SAL REAL SIN TENER EN CUENTA LA TEMPERATURA
	LDI				R18, 03				; LINEA DE ENTRADA EN EL CONVERTIDOS PCF8591
	LDS				R26, RAMC_SAL		; NUMERO DE LECTURAS Ó CALIBRACION DE 50 A 180
	RCALL			PCF
	CPI				R24, 60				; PARA VER SI ES MAYOR DE 60
	LDI				R25, 60
	SUB				R25, R24


DIV:
	CLR				R5
DIV_A:
	CPI				R24, 10
	BRLO			DIV_B
	INC				R5
	SUBI			R25, 10
	RJMP			DIV_A
DIV_B:
	RET		
