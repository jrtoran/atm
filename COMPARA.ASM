; COMPARA R18 CON R25 
; SI R18 ES MAYOR QUE R25 EL BIT CERO DEL REGISTRO CONTROL (R20)ES PUESTO A 1 EN CASO CONTRARIO A CERO

COMP:
	PUSH		R18						; SALVA EL REGISTRO EN LA PILA
	PUSH		R25
	SBRC		R25, 7					; SI EL BIT 7 ES UNO ES QUE ES MAYOR DE 125 VA A COMP1
	RJMP		COMP1
	SBRC		R18,7  					; SI EL BIT 7 ES CERO SALTA LA SIGUIENTE INSTRUCION SI NO ES QUE ES MAYOR
	RJMP		MAYOR
	SUB			R18, R25				; RESTA LOS DOS REGISTROS
	BRGE		MAYOR
	RJMP		MENOR
MAYOR:
	SBR			CONTROL, 0B00000001		; PONE A 1 EL BIT CERO DEL REGISTRO CONTROL PORQUE ES MAYOR R18 QUE R25
	POP			R25
	POP			R18						; REPONE EL REGISTRO DE LA PILA
	RET
MENOR:
	CBR			CONTROL, 0B00000001		; PONE A CERO EL BIT CERO PORQUE ES MEDOR R18 QUE R25
	POP			R25
	POP			R18
	RET
; SI R25 ES MAYOR DE 127 ( EL BIT 7 ES UNO )
COMP1:
	SBRS		R18,7					; SI EL BIT 7 ES UNO VA A MENOR
	RJMP		MENOR
	SUB			R18,R25
	BRGE		MAYOR
	RJMP		MENOR
