	BRLO  RADC4
	INC   R2
	RJMP  RADC3
RADC4:
	MOV   R3, R24
	LDI   R18, 48
	ADD   R1, R18
	ADD   R2, R18
	ADD   R3, R18
	CP			R18, R1		; COMPARA PARA VER SI ES CERO Y ELIMINARLO PONIENDO UN ESPACIO EN BLANCO
	BRNE		RADC5  		; SI NO ES CERO SALTA A RADC5
	LDI			R18, 32		; CARGA EL CODIGO DEL ESPACIO PARA
	MOV			R1, R18		; PASARLO A R1
RADC5:
	POP		 R18
	POP		R24
	RET
