; RUTINA CONVERSION DATO EN MEMORIA R24 EN DECIMAL resultado en R1,R2,R3 CENTENAS DECENAS Y UNIDADES
RADC:
	PUSH	R24
	PUSH	R18
	CLR   R1
	CLR   R2
	CLR   R3
	CPI   R24, 100
	BRLO  RADC2
	INC   R1
RADC1:	
