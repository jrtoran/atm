; PARA LA OPCION REMOTO CONTROL DE LAS FUNCIONES POR CABLE PLANO DE 10 HILOS



PRO_CONTROL:
	SBIC			PINB, 7			; SALTA LA SIGUENTE INSTRUCION SI ES CERO
	RCALL			ENCENDER_FUENTE_R
	SBIS			PINB, 7			; SALTA LA SIGUIENTE INSTRUCION SI ES UNO
	RCALL			APAGAR_FUENTE_R
	SBIS			PIND, 3			; EL LA ENTRADA DE PH + / -
	RJMP			PRO_CONTROL_1	; PARA LEER SOLO LINEA 5 DEL PUERTO B
	SBIS			PINB, 6
	CBI				PORTD,b_acido	; ENCIENDE BOMBA DE ACIDO
	SBIC			PINB, 6
	SBI				PORTD,b_acido	; APAGA BOMBA DE ACIDO
	RET

PRO_CONTROL_1:
	SBIS			PINB, 5
	CBI				PORTD,B_ACIDO		; ENCIENDE BOMBA DE ACIDO
	SBIC			PINB, 5
	SBI				PORTD,B_ACIDO		; APAGA BOMBA DE ACIDO
	RET


						; SI NO TIENE SALTA ESTA INSTRUCION
APAGAR_FUENTE_R:
;	SBIS			PINB, E_FUENTE		; salta la siguientre inst. si es UNO
	CBI				PORTB, 0			; SALIDA DEL 4N25 A CERO
;	SBIC			PINB, E_FUENTE		; salta la siguientre inst. si es CERO	
;	SBI   			DDRB, 1				; SALIDA DEL PC817 A UNO APAGADA
	CBI				PORTD, 4			; DESACIVA CONTROL REMOTO Y VENTILADOR
	RET

ENCENDER_FUENTE_R:
;	SBIS			PINB, E_FUENTE		; salta la siguientre inst. si es UNO
	SBI				PORTB, 0			; SALIDA DEL 4N25 A 1 
;	SBIC			PINB, E_FUENTE		; salta la siguientre inst. si es CERO
;	CBI   			DDRB, 1				; SALIDA DEL PC817 A UNO APAGADA
	SBI				PORTD, 4			; ACTIVA CONTROL REMOTO Y VENTILADOR
	RET								
