; RUTINA DE RETARDO PARA VALENCIA DE 240 SEGUNDOS
RETARDO240:
		CLR LEC_REL


RETARDO240_A:
	CALL			LEC_TEC				; LEE EL TECLADO
LDI R18,165 ; CODIGO ASCI DE LA R
STS $9A9,R18
	CPI				R17,0B01111111		; SI SE PULSA LA TECLA SALIR
	BREQ			RETARDO240_B		; VA A RETARDO240_B Y SALE
	MOV				R25,LEC_REL
	LDI				R24,240
	SUB				R24,R25
	STS				800,R24
	CALL			RADC
	STS				$761,R1
	STS				$762,R2
	STS				$763,R3

	LDI				R16,212				; ESCRIBIR LINEA 4
	CALL			ENVINT				; En subrrutina Env_doi Envia instrucion para escribir en linea 1
	LDI				YH, HIGH(RETARDO_PRO)		; POSICION BIT ALTO DEL TEXTO A ENVIAR ram en 070
	LDI				YL, LOW(RETARDO_PRO)		; POSICION BIT BAJO DEL TEXTO A ENVIAR
	LDI				R17,20				; NUMERO DE DIGITOS A ENVIAR A PANTALLA
	CALL			ENVIARTEXTO			; RUTINA EN ESTE FICHERO PRECISA SUBRRUTINA ENV_DOI	
	CPI				LEC_REL, 240
	BRNE			RETARDO240_A
RETARDO240_B:
	CALL			NO_TECLA
	RET

