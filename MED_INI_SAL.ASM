	LDI				R18, P_SAL				; LINEA DE ENTRADA EN EL CONVERTIDOR PCF8591
;	CALL			PCF					; VUELVE A MEDIR 
	CALL			ADC_P
	MOV				R19, R24
;	STS				RAM_PRU, R24
	CALL			RADC				; PRESENTA EN LINEA 3 LA MEDICION DE SAL REAL
	STS				$242, R1			; DECENAS
	STS				$243, R2			; UNIDADES
	STS				$244, R3			; DECIMAS	
