;MEDICION DE LOS AMPERIOS DE CONSUMO
AMPERIOS:
	LDI				R18, 2			; LINEA A LEER
	LDI				R26, 100		; NUMERO DE LECTURAS
	CALL			PCF_B			; LEE LA TEMPERATURA
;	STS				RAM_AMPERIOS, R24
	
;	LDS				R24, RAM_PH			; LEE EL PH DE LA RAM
	CALL			RADC				; RUTINA PARA CONVERTIR EN ANALOGICO
	STS				$0C2, R1
	STS				$0C3, R2			; RESULTADO DE DECENAS
	STS				$0C4, R3			; RESULTADO UNIDADES		



	LDI				R16, 192			; 11000000 escribir en linea 2 linea (192)( linea 4 seria 212)
	CALL			ENVINT
	LDI				YH, HIGH (L_AMPERIOS)	; POSICION ALTA DEL TAXTO EN RAM 080
	LDI				YL, LOW  (L_AMPERIOS)	; direccion BAJA DEL TEXTO EN RAM      	
  	LDI				R17,16 				; bytes a enviar 
	CALL			ENVIARTEXTO			; RUTINA PARA ENVIAR TEXTO A PANTALLA
	RET
